/*
IDENTIFICANDO EL TIPO DE VARIABLE
*/

//---> tipo de dato : String (Cadena de texto)
let nombre = "Rita Medina";
console.log(nombre);
console.log(typeof(nombre));

//---> tipo de dato : integer (Entero)
let edad = 25;
console.log(edad);
console.log(typeof(edad));

//---> tipo de dato : float (real)
let estatura = 1.56;
console.log(estatura);
console.log(typeof(estatura));

//---> tipo de dato : boolean (logico)
let estado = true;
console.log(estado);
console.log(typeof(estado));
